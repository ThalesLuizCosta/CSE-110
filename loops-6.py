people = ['Thales', 'Pamela']

print()
for name in people:
    print(name)

index = 0
while index < len(people):
    print(people[index])
    index = index + 1
print()

# Neste caso os dois tipos de loopings fazem a mesma coisa, só que com a diferença de que o looping "for" é mais simples para ser colocado em código com menos funções a serem inseridas.