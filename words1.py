# Uma variável pode ser criada para conter uma função que poderá ser chamada em qualquer momento dentro da função específica a ser desempenhada

sentence = 'The dog is named Caramelo'

print(sentence.upper()) # nesse caso é mostrado a letra inteira maiúscula

print(sentence.lower()) # nesse caso é mostrado a letra inteira minúscula

print(sentence.capitalize()) # nesse caso é mostrado a letra inicial maiúcula

print(sentence.count('m')) # nesse caso é mostrado a contagem da letra "m" (ou outra letra que for solicitado)

